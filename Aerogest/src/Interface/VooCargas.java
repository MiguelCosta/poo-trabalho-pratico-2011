    private void estado1(){
        jLabel_Cargas_adicionadas.setText(""+as.getVoosPorDia(da).get(codVoo).getCargasCarregadas().size());
        jLabel_Carregamento.setText("");
        jLabel_Carregamento_Total.setText(""+as.getVoosPorDia(da).get(codVoo).getTempoCarregamento());
        jLabel_Numero_Cargas.setText(""+as.getVoosPorDia(da).get(codVoo).getCarga().size());

        jButton_Carregar_Select.setEnabled(false);
    }

    private void init(){
        jLabel_Codigo.setText(codVoo);
        jList2.setListData(as.getVoosPorDia(da).get(codVoo).getCarga().toArray());
        GregorianCalendar g1 = as.getVoosPorDia(da).get(codVoo).getHoraPreparacao1();
        g1.add(GregorianCalendar.MINUTE, (int) as.getVoosPorDia(da).get(codVoo).getTempoCarregamento());
        long time = da.getTimeInMillis() - g1.getTimeInMillis();
        long minutos = (time%(60*60*1000))/(60*1000);
        long segundos = ((time%(60*60*1000))%(60*1000))/1000;

        jLabel_Carregamento_Restante.setText(""+minutos+":"+segundos);
        estado1();
    }

    private void actualizar(){
        jLabel_Cargas_adicionadas.setText(""+as.getVoosPorDia(da).get(codVoo).getCargasCarregadas().size());
    }

