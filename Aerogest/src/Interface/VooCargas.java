    private void jList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList2MouseClicked
        jLabel_Carregamento.setText(""+as.getVoosPorDia(da).get(codVoo).getCarga().
                get(jList2.getSelectedIndex()).getTempoCarregamento());
        Carga s = (Carga)jList2.getSelectedValue();
        ArrayList<String> ss = as.getVoosPorDia(da).get(codVoo).getCargasCarregadas();
        if (!ss.contains(s.getCodigo()))
            jButton_Carregar_Select.setEnabled(true);
        else
            jButton_Carregar_Select.setEnabled(false);
    }//GEN-LAST:event_jList2MouseClicked

    private void jButton_Carregar_SelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Carregar_SelectActionPerformed
        Carga s = (Carga)jList2.getSelectedValue();
        ArrayList<Carga> ss = as.getVoosPorDia(da).get(codVoo).getCarga();

        as.getVoosPorDia(da).get(codVoo).embarqueCarga(s);

        actualizar();
        jList2.setSelectedValue(null, false);
        jButton_Carregar_Select.setEnabled(false);

         if (as.getVoosPorDia(da).get(codVoo).getCarga().size() ==
                as.getVoosPorDia(da).get(codVoo).getCargasCarregadas().size())
            JOptionPane.showMessageDialog(rootPane, "Cargas já todas carregadas");
    }//GEN-LAST:event_jButton_Carregar_SelectActionPerformed

    private void jButton_Carregar_TodasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Carregar_TodasActionPerformed
        if (as.getVoosPorDia(da).get(codVoo).getCarga().size() ==
                as.getVoosPorDia(da).get(codVoo).getCargasCarregadas().size())
            JOptionPane.showMessageDialog(rootPane, "Cargas já todas carregadas");
        else
            as.getVoosPorDia(da).get(codVoo).embarqueCargaALL();
        actualizar();
    }//GEN-LAST:event_jButton_Carregar_TodasActionPerformed

